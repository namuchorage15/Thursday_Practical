public class Caste{
    public static void main(String[] args) {
        int i = 81;
        char C= (char)i;
        System.out.println("i ="+i+" & c ="+C);
        float d = 323.142f;
        int j = (int)d;
        System.out.println("d ="+d+" & j ="+j);
        float f =72.38f;
        int k = (int)f;
        System.out.println("f ="+f+" & k ="+k);
        byte l = (byte)d;
        System.out.println("d ="+d+" & l ="+l);
 }
}